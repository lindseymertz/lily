const verticals: ServiceRequest['vertical'][] = ['Restaurant', 'Fuel', 'Grocery'];
const issueCategories: ServiceRequest['issueCategory'][] = ['API Error', 'Billing Inquiry', 'Refund Request', 'Network Issues', 'Software Integration'];
const statuses: ServiceRequest['status'][] = ['Resolved', 'In Progress'];
const urgencies: ServiceRequest['urgency'][] = ['Low', 'Medium', 'High'];
const priorities: ServiceRequest['priority'][] = ['Low', 'Medium', 'High'];
const accountHealths: ServiceRequest['accountHealth'][] = ['Advocate', 'Engaged', 'Neutral', 'Skeptic', 'Churn Risk'];

function generateAdditionalData(startId: number, count: number): ServiceRequest[] {
  const additionalData: ServiceRequest[] = [];

  for (let i = 0; i < count; i++) {
    const id = startId + i;
    const vertical = verticals[Math.floor(Math.random() * verticals.length)];
    const companyName = `${vertical} Co ${id}`;

    const requestDate = new Date(2025, Math.floor(Math.random() * 12), Math.floor(Math.random() * 28) + 1);
    const timeToResolution = Math.floor(Math.random() * 90) + 5;
    const resolutionDate = new Date(requestDate);
    resolutionDate.setDate(resolutionDate.getDate() + timeToResolution);

    additionalData.push({
      requestId: `REG-${String(id).padStart(5, '0')}`,
      accountName: companyName,
      vertical,
      siteCount: Math.floor(Math.random() * 600) + 1,
      issueCategory: issueCategories[Math.floor(Math.random() * issueCategories.length)],
      requestDate: requestDate.toISOString().split('T')[0],
      status: statuses[Math.floor(Math.random() * statuses.length)],
      urgency: urgencies[Math.floor(Math.random() * urgencies.length)],
      priority: priorities[Math.floor(Math.random() * priorities.length)],
      timeToRespond: Math.floor(Math.random() * 24) + 1,
      timeToResolution,
      resolutionDate: resolutionDate.toISOString().split('T')[0],
      accountHealth: accountHealths[Math.floor(Math.random() * accountHealths.length)]
    });
  }

  return additionalData;
}

